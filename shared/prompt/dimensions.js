/**
 * 获取风格的话题维度提示
 * @param {string} style - 风格名称
 * @returns {string} 维度提示文本
 */
export function getDimensionHint(style) {
  const dimensions = styleDimensions[style] || [];
  if (dimensions.length === 0) {
    return '';
  }
  return `\n话题维度参考（请从中选择不同维度，避免重复）：${dimensions.join('、')}`;
}

